#include <stdio.h>

void main(){
    int number = 20 ; 
    float point = 2.5 ; 
    char a = 'a' ; 


    //  Types of Format Specifiers
    //   • %d is used to print integers
    printf("the number is %d \n", number);
    //   • %f is used to print real number
    printf("the floating value is %f \n", point);
    //   • %c is used to print characters
    printf("the character is %c \n", a);
}